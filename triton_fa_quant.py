def is_hip():
    return triton.runtime.driver.active.get_current_target().backend == "hip"


def is_cuda():
    return triton.runtime.driver.active.get_current_target().backend == "cuda"


def supports_host_descriptor():
    return is_cuda() and torch.cuda.get_device_capability()[0] >= 9


def is_blackwell():
    return is_cuda() and torch.cuda.get_device_capability()[0] == 10


def is_hopper():
    return is_cuda() and torch.cuda.get_device_capability()[0] == 9


@triton.jit
def _attn_fwd_inner(acc, l_i, m_i, q,  #
                    desc_k, desc_v,  #
                    offset_y, dtype: tl.constexpr, start_m, qk_scale,  #
                    BLOCK_M: tl.constexpr, HEAD_DIM: tl.constexpr, BLOCK_N: tl.constexpr,  #
                    STAGE: tl.constexpr, offs_m: tl.constexpr, offs_n: tl.constexpr,  #
                    N_CTX: tl.constexpr, warp_specialize: tl.constexpr, IS_HOPPER: tl.constexpr):
    m_cur_ij = tl.zeros([BLOCK_M], dtype=tl.float32)
    alpha_cur = tl.zeros([BLOCK_M], dtype=tl.float32)
    ##NOTE causal is false, stage ==3
    # range of values handled by this stage
    if STAGE == 1:
        lo, hi = 0, start_m * BLOCK_M
    elif STAGE == 2:
        lo, hi = start_m * BLOCK_M, (start_m + 1) * BLOCK_M
        lo = tl.multiple_of(lo, BLOCK_M)
    # causal = False
    else:
        lo, hi = 0, N_CTX
    offsetk_y = offset_y + lo
    if dtype == tl.float8e5:
        offsetv_y = offset_y * HEAD_DIM + lo
    else:
        offsetv_y = offset_y + lo
    # loop over k, v and update accumulator
    for start_n in tl.range(lo, hi, BLOCK_N, warp_specialize=warp_specialize):
        start_n = tl.multiple_of(start_n, BLOCK_N)
        # -- compute qk ----
        k = desc_k.load([offsetk_y, 0]).T
        qk = tl.dot(q, k)
        if STAGE == 2:
            mask = offs_m[:, None] >= (start_n + offs_n[None, :])
            qk = qk * qk_scale + tl.where(mask, 0, -1.0e6)
            m_ij = tl.maximum(m_i, tl.max(qk, 1))
            qk -= m_ij[:, None]
        else:
            global_max = True
            if not global_max:
                m_cur_ij = tl.max(qk, 1) * qk_scale
                m_ij = tl.maximum(m_i, m_cur_ij)
                qk = qk * qk_scale - m_cur_ij[:, None]
            else:
                m_ij = tl.maximum(m_i, tl.max(qk, 1) * qk_scale)
                qk = qk * qk_scale - m_ij[:, None]
        p = tl.math.exp2(qk)

        # -- compute correction factor
        alpha = tl.math.exp2(m_i - m_ij)
        if not global_max:
            alpha_cur = tl.math.exp2(m_cur_ij - m_ij)
        l_ij = tl.sum(p, 1)
        # -- update output accumulator --
        if not IS_HOPPER and warp_specialize and BLOCK_M == 128 and HEAD_DIM == 128:
            BM: tl.constexpr = acc.shape[0]
            BN: tl.constexpr = acc.shape[1]
            acc0, acc1 = acc.reshape([BM, 2, BN // 2]).permute(0, 2, 1).split()
            acc0 = acc0 * alpha[:, None]
            acc1 = acc1 * alpha[:, None]
            acc = tl.join(acc0, acc1).permute(0, 2, 1).reshape([BM, BN])
        else:
            acc = acc * alpha[:, None]
        # prepare p and v for the dot
        if dtype == tl.float8e5:
            v = desc_v.load([0, offsetv_y]).T
        else:
            v = desc_v.load([offsetv_y, 0])
        
        int_4 = True
        if int_4:
            Z = -8  # 固定zero_point
            SCALE = 1/15  # 固定scale
            
            # 量化计算：(p / SCALE) + Z，然后四舍五入
            # 由于P在0-1之间，(p / SCALE)范围是0-15，加Z(-8)后范围是-8到7，无需clip
            
            # 使用 floor(x + 0.5) 模拟四舍五入
            # NOTE
            value_to_round = (p / SCALE) + Z
            rounded_value = tl.floor(value_to_round + 0.5)
            
            # 正确转换（不再需要 fp_downcast_rounding）
            p_quant = tl.cast(rounded_value, tl.int8)

            # p_quant = tl.cast(tl.round((p / SCALE) + Z), tl.int8)
            # p_quant = tl.cast((p / SCALE) + Z, tl.int8) # , fp_downcast_rounding="rtne"
            
            # 反量化：恢复为float32
            p = tl.cast(p_quant, tl.float32)
            p = (p - Z) * SCALE

        p = p.to(dtype)

        # note that this non transposed v for FP8 is only supported on Blackwell
        if not global_max:
            acc += tl.dot(p, v) * alpha_cur[:, None]
        else:
            acc = tl.dot(p, v, acc)
        # update m_i and l_i
        # place this at the end of the loop to reduce register pressure
        if not global_max:
            l_i = l_i * alpha + l_ij * alpha_cur
        else:
            l_i = l_i * alpha + l_ij 
        m_i = m_ij
        offsetk_y += BLOCK_N
        offsetv_y += BLOCK_N
    return acc, l_i, m_i


def _host_descriptor_pre_hook(nargs):
    BLOCK_M = nargs["BLOCK_M"]
    BLOCK_N = nargs["BLOCK_N"]
    HEAD_DIM = nargs["HEAD_DIM"]
    if not isinstance(nargs["desc_q"], TensorDescriptor):
        return
    nargs["desc_q"].block_shape = [BLOCK_M, HEAD_DIM]
    if nargs["FP8_OUTPUT"]:
        nargs["desc_v"].block_shape = [HEAD_DIM, BLOCK_N]
    else:
        nargs["desc_v"].block_shape = [BLOCK_N, HEAD_DIM]
    nargs["desc_k"].block_shape = [BLOCK_N, HEAD_DIM]
    nargs["desc_o"].block_shape = [BLOCK_M, HEAD_DIM]


